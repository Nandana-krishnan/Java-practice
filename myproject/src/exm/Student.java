package exm;
//Encapsulation
public class Student {
	private String name;
	private int age;
	public void setName(String NNAMA) {
		name = NNAMA;
	}
	public String getName() {
		return name;
	}
	public int setAge(int newAge) {
		age = newAge;
		return age ;
	}
	public int getAge()
	{
		return age;
	}
}
